
public class Forloops {

	public static void main(String[] args) 
	{
		int x,var;
	for(x=2,var=99;x<=100;x=x+2,var=var-2 )
		{
		System.out.println(x +" "+var);
		}
	
		int a;
		for(a=1;a<50;a++)
		{
			System.out.println(a);
			
			break;
		}
		
		int b=2;
		for(;b<=20/2;b++)
		{
			if(b%2==0)
			{
				
			System.out.println(b);
		}
		}
	}
}




